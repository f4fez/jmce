/**
 * Teletext characters generator Malland SAA5050
 * <p>
 * Can generate 96 alphanumeric and 64 ghaphics character. In addtition
 * the first 32 character are used as control to determine the
 * attribute of the character.
 * <p>
 * <h2>Control codes</h2>
 * <pre>
 * 00  Unused                            10  Unused
 * 01  Alpha Red                         11  Graphics Red
 * 02  Alpha Green                       12  Graphics Green
 * 03  Alpha Yellow                      13  Graphics Yellow
 * 04  Alpha Blue                        14  Graphics Blue
 * 05  Alpha Magenta                     15  Graphics Magenta
 * 06  Alpha Cyan                        16  Graphics Cyan
 * 07  Alpha White*                      17  Graphics White
 * 08  Flash                             18  Conceal Display
 * 09  Steady(1)                         19  Contiguous Graphics(1)
 * 0A  End Box(1)                        1A  Separated Graphics
 * 0B  Start Box                         1B  Unused
 * 0C  Normal Height(1)                  1C  Black Background
 * 0D  Double Height                     1D  New Background
 * 0E  Unused                            1E  Hold Graphics
 * 0F  Unused                            1F  Release Graphics(1)
 * (1) Default setting
 *</pre>
 * <p>
 * <h2>Property</h2>
 * <p>
 * <tt>dotSize</tt> Set the size in pixel of a single dot. Default is
 * 2.<p>
 * <p>
 * @author Mario Viara
 * @version 1.00
 * @since 1.01
 */
public class SAA5050 extends AbstractPeripheral implements SwingHardware,MemoryWriteListener
{
	public final static Color colors[] = {Color.red,Color.green,Color.yellow,Color.blue,Color.magenta,Color.cyan,Color.white};
	private DisplaySAA5050 dsp ;
	private Memory m = null;
	private int base = 0;
	private int scroll = 0;
	private boolean doBlink;
	static private final int dotSize = 2;
	static private final int numCols = 40;
	static private final int numRows = 24;
	static private final int sizeRow = 80;
	
	/**
	 * Inner class with the Swing component
	 */
	class DisplaySAA5050 extends JComponent 
	{
		private static final long serialVersionUID = 1L;
		Color fa = colors[6];
		Color fg = colors[6];
		Color bg = Color.black;
		int fontGraphics[];
		int fontAlpha[];
		int lastCode = 32;
		boolean graphics = false;
		boolean doubleHeight = false;
		private boolean skip[] = new boolean[256];
		private boolean blink;

		DisplaySAA5050()
		{
			setPreferredSize(new Dimension(numCols * 6 * dotSize,numRows * 10 * dotSize));
		}

		public void updateComponent()
		{
			repaint();
		}

		public void paintComponent(Graphics g)
		{
			Insets insets = getInsets();

			g.translate(insets.left,insets.top);

			int pos = scroll;
			fa = fg = colors[6];
			fontAlpha = Saa5050FontNormal.font;
			fontGraphics = Saa5050FontGraphics.font;
			lastCode = 32;
			graphics = false;
			doubleHeight = false;
			bg = Color.black;
			blink = false;

			for (int r = 0 ; r < numRows ; r++)
			{
				doubleHeight = false;

				for (int c = 0 ; c < numCols ; c++)
				{
					try
					{
						if (!skip[c])
						{
							teletext(g,c,r,m.getMemory(base+pos+c));
							skip[c] = doubleHeight;
						}
						else
							skip[c] = false;
					}
					catch (Exception ignore)
					{
					}
				}

				pos += sizeRow;

			}
		}

		void teletext(Graphics g,int c,int r,int code)
		{
			switch (code)
			{
					// Blink
				case	0x08:
					blink = doBlink;
					break;

					// Steady
				case	0x09:
					blink = false;
					break;

					// Normal height
				case	0x0C:
					doubleHeight = false;
					break;

					// Double height
				case	0x0d:
					doubleHeight = true;
					break;

					// Not used ignored
				case	0:
				case	10:
					break;

					// Alphanumeric  color
				case	1:
				case	2:
				case	3:
				case	4:
				case	5:
				case	6:
				case	7:
					fa = colors[code - 1];
					graphics = false;
					break;

					// Graphics  color
				case	0x11:
				case	0x12:
				case	0x13:
				case	0x14:
				case	0x15:
				case	0x16:
				case	0x17:
					fg = colors[code - 0x11];
					graphics = true;
					break;

					// Contiguos graphics
				case	0x19:
					fontGraphics = Saa5050FontGraphics.font;
					break;

					// Separated graphics
				case	0x1a:
					fontGraphics = Saa5050FontGraphics2.font;
					break;

					// Black backgrund
				case	0x1c:
					bg = Color.black;
					break;

					// New blackGround
				case	0x1d:
					bg = graphics ? fg : fa;
					break;
			}



		//code &= 127;

			if (code  < 32)
				code = 32;

			lastCode = code;
			if (graphics)
				paintChar(g,c,r,code,fg,bg,fontGraphics);
			else
				paintChar(g,c,r,code,fa,bg,fontAlpha);

		}

		void drawLine(Graphics g,int x,int y,Color fg,Color bg,int font[],int pos)
		{
			for (int x1 = 0 ; x1 < 6 ; x1++)
			{
				if (font[pos] == 0)
					g.setColor(bg);
				else
					g.setColor(fg);
				g.fillRect(x,y,dotSize,dotSize);
				x += dotSize;
				pos++;
			}

		}

		void paintChar(Graphics g,int c,int r,int code,Color fg,Color bg,int font[])
		{
			int y = r * 10 * dotSize;
			int pos = (code - 32) * 60;
			Color tmp;

			if ((code & 0x80) != 0)
			{

				tmp = fg;
				fg = bg;
				bg = tmp;
				code &= 127;
			}

			if (blink)
			{
				tmp = fg;
				fg = bg;
				bg = tmp;
			}

			for (int y1 = 0 ; y1 < 10 ; y1++)
			{
				int x = c * 6 * dotSize;

				drawLine(g,x,y,fg,bg,font,pos);

				if (doubleHeight)
				{
					y += dotSize;
					drawLine(g,x,y,fg,bg,font,pos);
				}

				y += dotSize;
				pos += 6;

			}
		}
	}

	public SAA5050()
	{
		setName("SAA5050");
		dsp = new DisplaySAA5050();
	}

	public void setMemory(Memory m)
	{
		this.m = m;
	}

	public void setAddress(int a)
	{
		base = a;
	}

	public java.awt.Component getComponent()
	{
		return dsp;
	}


	/**
	 * Get the scroll register.
	 */
	public int getScroll()
	{
		return this.scroll;
	}

	/**
	 * Set the scroll register.
	 * <p>
	 * The scroll register determine the first character that will
	 * be displayed.
	 */
	public void setScroll(int scroll)
	{
		this.scroll = scroll;
		dsp.repaint();
	}

	public void registerCPU(CPU cpu) throws SIMException
	{
		super.registerCPU(cpu);

		for (int i = 0 ; i < 2048 ; i++)
			m.addMemoryWriteListener(base+i,this);

		jmce.util.Timer timer = new jmce.util.Timer(500,true,new jmce.util.TimerListener()
		{
			public void timerExpired()
			{
				doBlink = !doBlink;
				dsp.repaint();
			}
		});

		cpu.addTimerMs(timer);


	}


	public void	writeMemory(jmce.sim.Memory m,int address,int value,int oldValue) throws SIMException
	{
		dsp.repaint();

	}
}

